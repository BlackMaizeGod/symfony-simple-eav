    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Product::class);
    }

    /**
     * @return array
     * @throws DBALException
     */
    public function getBestsellersIds(): array
    {
        $connection = $this->getEntityManager()->getConnection();
        $query = $connection->prepare('SELECT product.id, SUM(sale.saled_qty) as saled_qty_sum  FROM `product` INNER JOIN sale on product.id = sale.product_id GROUP BY product_id ORDER BY saled_qty_sum DESC');
        $query->execute();
        $result = $query->fetchAll();

        return array_column($result, 'saled_qty_sum','id');
    }

